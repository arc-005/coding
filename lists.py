# create a list using []
a = [1,2,3,4,5]

# print the list using print () functions
print(a)

# Access using index using a[0], a[1]
print(a[2])

# Change the value of list using 
a[0] = 4
print (a)

# we can create a list with items of different types
c = [45, "ashish", False, 6.9]
print(c)

# list slicing
friends = ["Harry","Rohan", "samid", "yash", 69, "amit"]
print(friends[0:4])
print(friends[-4:])